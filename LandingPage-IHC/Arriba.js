$(document).ready(function (){
    $('.Inicio').click(function (){
        $('body,html').animate({
            scrollTop: '0px'
        });
    })

    $('.nuestrosServicios').click(function (){
        $('body,html').animate({
            scrollTop: '785px'
        });
    })
    $('.NuestroProducto').click(function (){
        $('body,html').animate({
            scrollTop: '2850px'
        });
    })
    $('.TrabajaconNosotros').click(function (){
        $('body,html').animate({
            scrollTop: '4000px'
        });
    })
})